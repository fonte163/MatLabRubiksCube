function [] = initdraw()
	% "zeichnet" Würfel zum ersten Mal
	figure
	axis off
  	% Mitten
  	patch([1,2,2,1],[2,2,1,1],[3,3,3,3],'w');
  	patch([1,2,2,1],[0,0,0,0],[2,2,1,1],'r');
